/// Generate Rust source code with `#[repr(C)]` structs, parent embedding,
/// const offset tables, bitfield accessors, UFunction address constants,
/// UEnum variant modules, size asserts, and per-package modules.
pub fn generate_rust(
    classes: &[ClassDump],
    enums: &[EnumDump],
    globals: Option<&ScanResults>,
    base: usize,
) -> String {
    let mut out = String::with_capacity(512 * 1024);
    emit_header(&mut out);
    emit_common_types(&mut out);
    if let Some(g) = globals {
        emit_globals(&mut out, g, base);
    }
    emit_enums_module(&mut out, enums);
    emit_package_modules(&mut out, classes);
    out
}

/// Generate pointer-chain constants for reaching the local player pawn from GWorld.
///
/// Emits offsets for the standard UE5 chain:
///   GWorld -> UWorld.OwningGameInstance -> UGameInstance.LocalPlayers[0]
///          -> ULocalPlayer.PlayerController -> APlayerController.AcknowledgedPawn
///
/// A WARNING comment is emitted for any step missing from the dump.
pub fn generate_chain(classes: &[ClassDump], gworld_offset: usize) -> String {
    let mut out = String::with_capacity(1024);

    writeln!(out, "// Auto-generated player-pawn pointer chain by ue5-dumper").unwrap();
    writeln!(out, "// Generated: {}", timestamp()).unwrap();
    writeln!(out).unwrap();

    writeln!(out, "pub const OFF_GWORLD: usize = {gworld_offset:#X};").unwrap();

    if let Some(off) = find_property_offset(classes, "World", "OwningGameInstance") {
        writeln!(out, "pub const OFF_GAME_INSTANCE: usize = {off:#X};").unwrap();
    } else {
        writeln!(out, "// WARNING: World.OwningGameInstance not found in dump").unwrap();
    }

    if let Some(off) = find_property_offset(classes, "GameInstance", "LocalPlayers") {
        writeln!(out, "pub const OFF_LOCAL_PLAYERS: usize = {off:#X};").unwrap();
    } else {
        writeln!(out, "// WARNING: GameInstance.LocalPlayers not found in dump").unwrap();
    }

    if let Some(off) = find_property_offset(classes, "PlayerController", "Player") {
        writeln!(out, "// Hint: PlayerController.Player = {off:#X}").unwrap();
    }
    if let Some(off) = find_property_offset(classes, "Player", "PlayerController") {
        writeln!(out, "pub const OFF_PLAYER_CONTROLLER: usize = {off:#X}; // UPlayer.PlayerController").unwrap();
    } else {
        writeln!(out, "// WARNING: Player.PlayerController not found in dump").unwrap();
    }

    if let Some(off) = find_property_offset(classes, "PlayerController", "AcknowledgedPawn") {
        writeln!(out, "pub const OFF_ACKNOWLEDGED_PAWN: usize = {off:#X};").unwrap();
    } else {
        writeln!(out, "// WARNING: PlayerController.AcknowledgedPawn not found in dump").unwrap();
    }

    out
}

